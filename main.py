async def message_handler(bot: Robot, message: Message):
    """مدیریت پیام‌ها"""
    try:
        chat_id = message.chat_id
        text = message.text or ""
        text_lower = text.lower()
        
        # ذخیره تاریخچه
        if chat_id not in MESSAGES_HISTORY:
            MESSAGES_HISTORY[chat_id] = []
        MESSAGES_HISTORY[chat_id].append(message)
        
        # اگر ربات در این گروه فعال نیست
        if chat_id not in ACTIVE_GROUPS or not ACTIVE_GROUPS[chat_id]:
            return
        
        # ===== فیلتر لینک (فقط در گروه) =====
        if LINK_PATTERN.search(text) and message.is_group:
            await bot.delete_message(chat_id, message.message_id)
            await message.reply(LINK_WARN)
            return
        
        # ===== فیلتر بیوچک (فقط در گروه) =====
        if BIO_PATTERN.search(text_lower) and message.is_group:
            await bot.delete_message(chat_id, message.message_id)
            await message.reply(BIO_WARN)
            return
        
        # ===== فیلتر فحاشی (فقط در گروه) =====
        if BAD_WORDS:
            for word in BAD_WORDS:
                if word == text_lower and message.is_group:
                    await message.reply(FOSH_WARN)
                    await bot.delete_message(chat_id, message.message_id)
                    return
            for word in BAD_WORDS:
                if word in text_lower and message.is_group:
                    await message.reply(FOSH_WARN)
                    await bot.delete_message(chat_id, message.message_id)
                    return
        
        # ===== پاسخ‌های خودکار =====
        for key, replies in RANDOM_REPLY.items():
            if key == text_lower:
                await message.reply(random.choice(replies))
                return
        
        # ===== دستورات ویژه =====
        
        # ترجمه دو طرفه
        if text_lower.startswith("ترجمه"):
            text_to_translate = text.replace("ترجمه", "").strip()
            if text_to_translate:
                translated = translate_text(text_to_translate)
                await message.reply(translated)
            else:
                await message.reply("متنی برای ترجمه وارد نشده!\nمثال: ترجمه Hello")
            return
            
        if "/smaads " in text_lower:
            tads = text_lower.replace("/smaads ", "")
            for i in ACTIVE_GROUPS:
                await bot.send_message(chat_id=i, text=tads)
        # تاریخ
        if text_lower == "تاریخ":
            await message.reply(get_datetime())
            return
        
        # بیو/نقل قول
        if text_lower == "بیو":
            await message.reply(get_quote())
            return
        
        # حقیقت یا جسارت
        if text_lower == "حقیقت":
            await message.reply(get_truth())
            return
        if text_lower == "جرعت":
            await message.reply(get_dare())
            return
            
    except Exception as e:
        print(f"❌ خطا در پردازش پیام: {e}")

