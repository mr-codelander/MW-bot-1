def translate_text(text):
    """ترجمه دو طرفه: فارسی به انگلیسی یا انگلیسی به فارسی"""
    try:
        # تشخیص زبان متن
        # اگر متن شامل حروف فارسی باشد
        if any('\u0600' <= c <= '\u06FF' for c in text):
            # فارسی به انگلیسی
            target = 'en'
            from_lang = 'fa'
        else:
            # انگلیسی به فارسی
            target = 'fa'
            from_lang = 'en'
        
        url = "https://translate.googleapis.com/translate_a/single"
        params = {
            'client': 'gtx',
            'sl': from_lang,
            'tl': target,
            'dt': 't',
            'q': text
        }
        r = requests.get(url, params=params, timeout=5)
        result = r.json()
        translated = ''.join(part[0] for part in result[0] if part[0])
        
        # اضافه کردن نشان‌دهنده زبان مقصد
        if target == 'en':
            return f"🇬🇧 ترجمه به انگلیسی:\n{translated}"
        else:
            return f"🇮🇷 ترجمه به فارسی:\n{translated}"
            
    except:
        return "❌ خطا در ترجمه"

